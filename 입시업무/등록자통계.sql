SELECT HG_NAME 학과, COUNT(*) 합격자수
FROM IPSI.IPSI201, IPSI.IPSI104, IPSI.IPSI203
WHERE IW_YEAR = &입시년도
AND IW_JWHGCODE1 = HG_CODE and iw_year = hg_year
AND IW_YEAR = DR_YEAR AND IW_SHNO = DR_SHNO
and nvl(dr_drniymd, '00000000') <>  '00000000' --and nvl(dr_drhbymd, '00000000') = '00000000'
GROUP BY CUBE(HG_NAME)
ORDER BY HG_NAME