UPDATE AC1401 A SET SJ_JUMSU = (SELECT B.SJ_JUMSU
FROM ALEXIT.LMSAC1401 B
WHERE A.SJ_YEAR = B.SJ_YEAR AND A.SJ_HAKGI = B.SJ_HAKGI
AND A.SJ_HAKBUN = B.SJ_HAKBUN AND SUBSTR(A.SJ_GGGMCODE,3,5) = SUBSTR(B.SJ_GGGMCODE,3,5))
WHERE A.SJ_YEAR = &학년도 AND A.SJ_HAKGI = &학기
AND (A.SJ_YEAR, A.SJ_HAKGI, A.SJ_HAKBUN, A.SJ_GGGMCODE) IN (SELECT C.SJ_YEAR, C.SJ_HAKGI, C.SJ_HAKBUN, C.SJ_GGGMCODE
FROM AC1401 C, ALEXIT.LMSAC1401 D WHERE C.SJ_YEAR = A.SJ_YEAR AND C.SJ_HAKGI = A.SJ_HAKGI
AND C.SJ_YEAR = D.SJ_YEAR AND C.SJ_HAKGI = D.SJ_HAKGI
AND C.SJ_HAKBUN = D.SJ_HAKBUN AND SUBSTR(C.SJ_GGGMCODE,3,5) = SUBSTR(D.SJ_GGGMCODE,3,5))