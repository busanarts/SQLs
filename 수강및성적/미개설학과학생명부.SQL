HJ_HGCODE 원학과코드, DEPTNAME(HJ_HGCODE) 원학과명,  HJ_WTHGCODE 수강학과코드, DEPTNAME(HJ_WTHGCODE) 수강학과명
FROM AC12011,AC1121, TBL학과,
(SELECT DISTINCT GG_HGCODE FROM AC1303 WHERE GG_YEAR = '2010' AND GG_HAKGI = '2') B
WHERE HJ_STATE = '01'
AND HJ_HGCODE = HG_CODE AND HG_MCODE = 학과코드
AND HJ_WTHGCODE = GG_HGCODE(+)
AND GG_HGCODE IS NULL
ORDER BY HJ_HAKBUN