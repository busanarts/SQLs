SELECT HJ_KNAME 성명, HJ_JCODE1  || '-' || HJ_JCODE2 주민번호, ' ' 계급, HJ_EMAIL 이메일,
HJ_HSHPTEL 핸드폰, ' ' 전역년도, HJ_HAKNYUN 학년, HC_SIYMD 학적변동일자
FROM AC1202 A, AC12011, AC1104
WHERE SUBSTR(HJ_STATE,1,1)  = '1'
AND HC_HAKBUN = HJ_HAKBUN
AND HC_YEAR = '2009' AND HC_HAKGI = '2'
AND HC_SIYMD >= '2008'
AND SUBSTR(HJ_JCODE2,1,1) = '1'
AND HJ_STATE = HB_CODE
AND (HC_HAKBUN, HC_SIYMD) IN
(SELECT HC_HAKBUN, MAX(HC_SIYMD) FROM AC1202
WHERE HC_HAKBUN = A.HC_HAKBUN
GROUP BY HC_HAKBUN)
