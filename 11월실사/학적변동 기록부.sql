SELECT HG_KNAME 학과명, HJ_HAKBUN 학번, HJ_KNAME 성명,
DECODE(SUBSTR(HJ_STATE,1,1), '0', '재학', '1', '휴학', '2', '제적', '8', '졸업') 현학적상태, HJ_JCODE1 || '-' || HJ_JCODE2 주민번호,
HC_SCODE 학적변동코드, HB_NAME 학적변동명, HC_SIYMD 변동일자
FROM AC12011, AC1202, AC1104, AC1121
WHERE HJ_HAKBUN = HC_HAKBUN
AND HJ_HGCODE = HG_CODE
AND HC_SIYMD >= '20080301' AND HC_SIYMD < '20090301'
AND HC_SCODE = HB_CODE